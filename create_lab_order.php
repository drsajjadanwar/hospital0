function getPatientByMrn(PDO $pdo, string $mrn): array|false
{
    $st = $pdo->prepare(
        "SELECT mrn, full_name, gender, age, age_unit
         FROM patients
         WHERE mrn = ?
         LIMIT 1"
    );
    $st->execute([$mrn]);
    return $st->fetch(PDO::FETCH_ASSOC);
}

/**
 * Generates a unique 8-digit patient MRN not in the database.
 */
function generateUniqueMrn(PDO $pdo): string
{
    $st = $pdo->prepare("SELECT 1 FROM patients WHERE mrn = ?");
    do {
        $mrn = (string) mt_rand(10000000, 99999999);
        $st->execute([$mrn]);
    } while ($st->fetchColumn() !== false);
    return $mrn;
}

/**
 * Inserts a new patient with default values.
 */
function insertNewPatient(PDO $pdo, string $mrn, string $name, int $age): void
{
    $st = $pdo->prepare(
        "INSERT INTO patients (mrn, full_name, age, age_unit, gender)
         VALUES (?, ?, ?, 'years', 'N/A')"
    );
    $st->execute([$mrn, $name, $age]);
}


