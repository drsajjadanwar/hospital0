class Code128Fixed extends PDF_Code128
{
    function __construct()
    {
        parent::__construct();
        foreach (['A', 'B', 'C'] as $s) {
            if (isset($this->SetFrom[$s]) && !is_array($this->SetFrom[$s])) {
                $this->SetFrom[$s] = str_split($this->SetFrom[$s]);
            }
        }
    }
}

/*────────  custom PDF  ────────*/
class LabPDF extends Code128Fixed
{
    function __construct()
    {
        parent::__construct('P', 'mm', 'A4');

        /*---------------------------------------------------------
         * Register the custom font and **select it immediately**.
         * If registration fails, fall back gracefully to Arial.
         *--------------------------------------------------------*/
        try {
            $this->AddFont('MyriadPro-Regular', '', 'MyriadPro-Regular.php');
            $this->SetFont('MyriadPro-Regular', '', 10);   // ← active font
        } catch (Exception $e) {
            // Fallback – still allows PDF to generate
            $this->SetFont('Arial', '', 10);
        }
    }

    function family()
    {
        return $this->FontFamily;
    }

    function Header()
    {
        $W   = $this->GetPageWidth();
        $logo = __DIR__ . '/media/logo-print.png';
        if (is_readable($logo)) {
            $this->Image($logo, ($W - 80) / 2, 10, 80);
            $this->Ln(35);
        }
        $this->SetFont($this->FontFamily, '', 16);
        $this->SetTextColor(100, 100, 100);
        $this->Cell(0, 7, 'hospital0', 0, 1, 'C');
        $this->SetFont($this->FontFamily, '', 12);
        $this->Cell(0, 5, 'www.hospital0', 0, 1, 'C');
        $this->Ln(2);
        $this->SetDrawColor(150, 150, 150);
        $this->SetLineWidth(.4);
        $x = ($W - 140) / 2;
        $y = $this->GetY() + 2;
        $this->Line($x, $y, $x + 140, $y);
        $this->Ln(10);
    }

    function Footer()
    {
        global $ufull;
        $this->SetY(-20);
        $this->SetFont($this->FontFamily, '', 9);
        $this->SetTextColor(100, 100, 100);
        $now = new DateTime('now', new DateTimeZone('Asia/Karachi'));
        $this->Cell(
            0,
            6,
            'Invoice generated by ' . $ufull . ' on ' . $now->format('Y-m-d H:i:s T'),
            0,
            1,
            'C'
        );
        $this->Ln(1);
        $this->SetFont($this->FontFamily, '', 10);
        $this->Cell(0, 6, 'hospital0', 0, 1, 'C');
    }

    function Row($lab, $val, $labW = 45, $h = 7)
    {
        $w = $this->GetPageWidth() - $this->lMargin - $this->rMargin - $labW;
        $this->SetFont($this->FontFamily, '', 11);
        $this->Cell($labW, $h, $lab, 0, 0);
        $x = $this->GetX();
        $y = $this->GetY();
        $this->MultiCell($w, $h, $val);
        $this->SetXY($this->lMargin, max($y + $h, $this->GetY()));
    }

    function HeadRow($h1, $h2)
    {
        $tot = $this->GetPageWidth() - $this->lMargin - $this->rMargin;
        $w   = [($tot - 45), 45];
        $this->SetFont($this->FontFamily, '', 11);
        $this->SetFillColor(220, 220, 220);
        $this->Cell($w[0], 7, $h1, 1, 0, 'C', true);
        $this->Cell($w[1], 7, $h2, 1, 1, 'C', true);
        return $w;
    }

    function DataRow($row, $w)
    {
        $this->SetFont($this->FontFamily, '', 10);
        $this->SetDrawColor(200, 200, 200);
        $this->Rect($this->GetX(), $this->GetY(), $w[0], 6);
        $this->Cell($w[0], 6, $row[0], 0, 0);
        $this->Rect($this->GetX(), $this->GetY(), $w[1], 6);
        $this->Cell($w[1], 6, $row[1], 0, 1, 'R');
    }
}

