if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $act = $_POST['action'] ?? '';
    $pdo->beginTransaction();
    try {
        /* fetch or create patient */
        if ($act === 'fetch_patient') {
            $mrn = trim($_POST['mrn'] ?? '');

            if ($mrn !== '') { // MRN was entered, use existing logic
                if (!$pat = getPatientByMrn($pdo, $mrn)) {
                    $_SESSION['error_message'] = 'No patient with MRN "' . htmlspecialchars($mrn) . '"';
                    $_SESSION['create_order_mrn'] = $mrn;
                } else {
                    $_SESSION['fetched_patient_data'] = $pat;
                    $_SESSION['create_order_mrn']     = $mrn;
                }
            } else { // MRN is blank, create a new patient
                $fullName = trim($_POST['full_name'] ?? '');
                $age      = filter_var($_POST['age'] ?? '', FILTER_VALIDATE_INT);

                if (empty($fullName) || $age === false || $age <= 0) {
                    throw new Exception('Full Name and a valid Age are required when MRN is not provided.');
                }

                $newMrn = generateUniqueMrn($pdo);
                insertNewPatient($pdo, $newMrn, $fullName, $age);

                // Set session data for the newly created patient
                $_SESSION['fetched_patient_data'] = [
                    'mrn'       => $newMrn,
                    'full_name' => $fullName,
                    'gender'    => 'N/A',
                    'age'       => $age,
                    'age_unit'  => 'years',
                ];
                $_SESSION['create_order_mrn'] = $newMrn;
            }
            $pdo->commit();
            header('Location: create_lab_order.php');
            exit;
        }


        /* create order + PDF */
        if ($act === 'create_order') {
            $mrn      = trim($_POST['mrn'] ?? '');
            $selected = $_POST['selected_tests'] ?? [];
            if ($mrn === '') {
                throw new Exception('MRN missing.');
            }
            if (!$pat = getPatientByMrn($pdo, $mrn)) {
                throw new Exception('Invalid MRN.');
            }
            if (!$selected) {
                throw new Exception('Select at least one test.');
            }

            // validate tests
            $ph  = rtrim(str_repeat('?,', count($selected)), ',');
            $chk = $pdo->prepare(
                "SELECT test_id, test_name, test_price
                 FROM lab_tests
                 WHERE test_id IN ($ph) AND is_active = TRUE"
            );
            $chk->execute($selected);
            $rows = $chk->fetchAll(PDO::FETCH_ASSOC);
            if (count($rows) !== count($selected)) {
                throw new Exception('Inactive/invalid test selected.');
            }

            $total = array_sum(array_column($rows, 'test_price'));

            // create order
            $pdo->prepare(
                "INSERT INTO lab_orders
                 (patient_mrn, ordered_by_user, status, total_amount, order_date)
                 VALUES (?, ?, 'Pending', ?, NOW())"
            )->execute([$mrn, $uname, $total]);
            $oid = $pdo->lastInsertId();

            // items
            $ins = $pdo->prepare(
                "INSERT INTO lab_order_items (order_id, test_id)
                 VALUES (?, ?)"
            );
            foreach ($selected as $tid) {
                $ins->execute([$oid, $tid]);
            }

            /* PDF path */
            $dir = __DIR__ . '/lab_invoices/';
            if (!is_dir($dir)) {
                @mkdir($dir, 0775, true);
            }
            $safeMrn = preg_replace('/[^A-Za-z0-9_-]/', '_', $mrn);
            $file    = $dir . "LabInvoice_{$safeMrn}_{$oid}.pdf";
            for ($i = 1; file_exists($file); $i++) {
                $file = $dir . "LabInvoice_{$safeMrn}_{$oid}_{$i}.pdf";
            }
            $rel = 'lab_invoices/' . basename($file);

            /* generate PDF */
            $pdfDoc = new LabPDF();
            $f      = $pdfDoc->family();
            $pdfDoc->SetMargins(15, 10);
            $pdfDoc->SetAutoPageBreak(true, 25);
            $pdfDoc->AddPage();
            $pdfDoc->SetFont($f, '', 16);
            $pdfDoc->Cell(0, 8, 'LABORATORY ORDER INVOICE', 0, 1, 'C');
            $pdfDoc->Ln(8);

            $pdfDoc->SetFont($f, '', 12);
            $pdfDoc->Row('Patient Name:', $pat['full_name']);
            $pdfDoc->Row('MRN:', $pat['mrn']);
            $ageTxt = $pat['age'] > 0 ? $pat['age'] . ' ' . $pat['age_unit'] : 'N/A';
            $pdfDoc->Row('Age:', $ageTxt);
            $pdfDoc->Row('Gender:', $pat['gender']);
            $pdfDoc->Ln(5);
            $pdfDoc->Row('Order ID:', $oid);
            $pdfDoc->Row('Order Date:', date('d M Y, h:i A'));
            $pdfDoc->Ln(10);

            /* barcode */
            $barW = 80;
            $x    = ($pdfDoc->GetPageWidth() - $barW) / 2;
            $y    = $pdfDoc->GetY();
            $pdfDoc->Code128($x, $y, (string) $oid, $barW, 20);
            $pdfDoc->SetY($y + 22);

            $pdfDoc->Cell(0, 8, 'Ordered Tests:', 0, 1, 'L');
            $w = $pdfDoc->HeadRow('Test Name', 'Price (PKR)');
            foreach ($rows as $r) {
                $pdfDoc->DataRow(
                    [$r['test_name'], number_format($r['test_price'], 2)],
                    $w
                );
            }
            $pdfDoc->Ln(5);
            $pdfDoc->Cell($w[0], 8, 'Total Amount:', 0, 0, 'R');
            $pdfDoc->Cell(
                $w[1],
                8,
                'PKR ' . number_format($total, 2),
                1,
                1,
                'R'
            );

            $pdfDoc->Output('F', $file);

  /* ---------- NEW: auto-ledger entry ---------- */
            $pdo->prepare(
              "INSERT INTO labledger (datetime,description,amount,user)
               VALUES (NOW(),?,?,?)"
            )->execute([basename($file), $total, $uname]);  // credit = +ve amount

            $pdo->commit();
            $_SESSION['success_message'] = 'Lab order #' . $oid . ' created.';
            $_SESSION['pdfLink']        = $rel;
            $_SESSION['trigger_print']  = true;
            unset($_SESSION['fetched_patient_data'], $_SESSION['create_order_mrn']);
            header('Location: create_lab_order.php');
            exit;
        }

        $pdo->commit();
    } catch (Exception $e) {
        if ($pdo->inTransaction()) {
            $pdo->rollBack();
        }
        $_SESSION['error_message'] = 'Error: ' . $e->getMessage();
        header('Location: create_lab_order.php');
        exit;
    }
}

